    private void srediFormu() {
        TableModelStavkaRacuna tmsr = new TableModelStavkaRacuna(listaStavkiRacuna);
        tblStavkaRacuna.setModel(tmsr);

        UnosRacunaKKI.vratiKupce(cbKupac);
        prijavljeni = PrijavaKKI.prijavljeni;

        txtUkupanIznos.setText(String.valueOf(ukupanIznos));
        
        JTableHeader tblHeader = tblStavkaRacuna.getTableHeader();
        tblHeader.setFont(new Font("Calibri light", Font.PLAIN, 16));
    }

    public void dodajUTabelu(StavkaRacuna stavkaRacuna) {
        TableModelStavkaRacuna tmsr = (TableModelStavkaRacuna) tblStavkaRacuna.getModel();
        tmsr.dodajStavku(stavkaRacuna);
    }

    List<StavkaRacuna> getListaSstavkiRacuna() {
        TableModelStavkaRacuna tmsr = (TableModelStavkaRacuna) tblStavkaRacuna.getModel();
        return tmsr.getListaStavkiRacuna();
    }

    public void setListaStavkiRacuna(List<StavkaRacuna> listaStavkiRacuna) {
        this.listaStavkiRacuna = listaStavkiRacuna;
    }

    void dodajIznos(double iznos) {
        ukupanIznos = ukupanIznos + iznos;
        txtUkupanIznos.setText(String.valueOf(ukupanIznos));
    }

    private void postaviDatum() {
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
        String datum = sdf.format(new Date());
        txtDatum.setText(datum);
    }

    private void resetuj() {
        txtRacunID.setText("");
        txtDatum.setText("");
        txtUkupanIznos.setText("");
        ukupanIznos = 0;
        setListaStavkiRacuna(new ArrayList<>());
        TableModelStavkaRacuna tmsr = (TableModelStavkaRacuna) tblStavkaRacuna.getModel();
        tmsr.setListaStavkiRacuna(listaStavkiRacuna);
        tmsr.fireTableDataChanged();
        UnosRacunaKKI.vratiKupce(cbKupac);

        btnKreirajNoviRacun.setEnabled(true);

    }

    void izmeniTabelu(Ploca ploca, int kolicina) {
        TableModelStavkaRacuna tmrs = (TableModelStavkaRacuna) tblStavkaRacuna.getModel();
        listaStavkiRacuna = tmrs.getListaStavkiRacuna();
        for (StavkaRacuna stavkaRacuna : listaStavkiRacuna) {
            if (stavkaRacuna.getPloca().equals(ploca)) {
                System.out.println(stavkaRacuna.getPloca() + " = " + ploca );
                stavkaRacuna.setKolicina(stavkaRacuna.getKolicina() + kolicina);
                tmrs.setListaStavkiRacuna(listaStavkiRacuna);
            }
        }
    }
