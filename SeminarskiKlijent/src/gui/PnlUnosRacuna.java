    private void btnKreirajNoviRacunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajNoviRacunActionPerformed
        int racunID = UnosRacunaKKI.vratiID(txtRacunID);
        if (racunID <= 0) {
            JLabel label = new JLabel("Sistem ne može da kreira novi račun!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(null, label, "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        JLabel label = new JLabel("Sistem je kreirao novi račun!");
        label.setFont(new Font("Calibri light", Font.PLAIN, 16));
        JOptionPane.showMessageDialog(this, label);
        btnKreirajNoviRacun.setEnabled(false);
        postaviDatum();
    }//GEN-LAST:event_btnKreirajNoviRacunActionPerformed

    private void btnDodajStavkuRacunaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajStavkuRacunaActionPerformed
        if (txtRacunID.getText().isEmpty()) {
            JLabel label = new JLabel("Morate kreirati račun da biste dodali stavku!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(null, label, "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        racun = new Racun();
        racun.setRacunID(Integer.parseInt(txtRacunID.getText()));

        PnlUnosStavkeRacuna unosStavkeRacuna = new PnlUnosStavkeRacuna(this, racun);
        JDialog dialog = new JDialog((Dialog) SwingUtilities.getWindowAncestor(this), "Prodavnica gramofonskih ploča - unos stavke računa", true);
        dialog.setLayout(new BorderLayout());
        dialog.add(unosStavkeRacuna);
        dialog.pack();
        dialog.setLocationRelativeTo(null);
        dialog.setVisible(true);
    }//GEN-LAST:event_btnDodajStavkuRacunaActionPerformed

    private void btnObrisiStavkuRacunaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiStavkuRacunaActionPerformed
        if (tblStavkaRacuna.getSelectedRow() < 0) {
            JLabel label = new JLabel("Morate odabrati stavku koju želite da obrišete!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(null, label, "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int selektovanRed = tblStavkaRacuna.getSelectedRow();
        TableModelStavkaRacuna tmsr = (TableModelStavkaRacuna) tblStavkaRacuna.getModel();
        StavkaRacuna stavka = tmsr.vratiStavkuRacuna(selektovanRed);
        tmsr.obrisiStavku(selektovanRed);
        ukupanIznos = ukupanIznos - stavka.getIznos();
        txtUkupanIznos.setText(String.valueOf(ukupanIznos));
    }//GEN-LAST:event_btnObrisiStavkuRacunaActionPerformed

    private void btnSacuvajRacunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajRacunActionPerformed
        TableModelStavkaRacuna tmsr = (TableModelStavkaRacuna) tblStavkaRacuna.getModel();
        List<StavkaRacuna> listaStavkiRacuna = tmsr.getListaStavkiRacuna();

        if (listaStavkiRacuna.isEmpty()) {
            JLabel label = new JLabel("Račun mora imati bar jednu stavku!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(null, label, "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean uspesno = UnosRacunaKKI.sacuvajRacun(txtRacunID, cbKupac, txtDatum, txtUkupanIznos, listaStavkiRacuna);
        if (uspesno) {
            JLabel label = new JLabel("Sistem je uspešno sačuvao račun!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(this, label);
            resetuj();
        } else {
            JLabel label = new JLabel("Sistem ne može da zapamti račun!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(null, label, "Greška!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSacuvajRacunActionPerformed

