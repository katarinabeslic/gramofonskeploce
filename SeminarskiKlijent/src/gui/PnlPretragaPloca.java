    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        List<Ploca> listaPlocaNaOsnovuKriterijuma = PretragaPlocaKKI.traziPlocu(txtKriterijumZaPretragu, tblPloca);

        if (listaPlocaNaOsnovuKriterijuma.isEmpty()) {
            JLabel label = new JLabel("Sistem ne može da pronađe ploču po zadatim vrednostima!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(null, label, "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        JLabel label = new JLabel("Sistem je pronašao ploče po zadatim vrednostima!");
        label.setFont(new Font("Calibri light", Font.PLAIN, 16));
        JOptionPane.showMessageDialog(this, label);
    }//GEN-LAST:event_btnPretraziActionPerformed

