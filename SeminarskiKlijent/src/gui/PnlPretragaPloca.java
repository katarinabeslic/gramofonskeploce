    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        if (tblPloca.getSelectedRow() < 0) {
            JLabel label = new JLabel("Sistem ne može da nađe izabranu ploču!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(null, label, "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        TableModelPloca tmp = (TableModelPloca) tblPloca.getModel();
        ploca = tmp.vratiPlocu(tblPloca.getSelectedRow());

        PnlIzmenaPloce izmenaPloce = new PnlIzmenaPloce(this);
        JDialog dialog = new JDialog((Dialog) SwingUtilities.getWindowAncestor(this), "Prodavnica gramofonskih ploča - izmena ploče", true);
        dialog.setLayout(new BorderLayout());
        dialog.add(izmenaPloce);
        dialog.pack();
        dialog.setLocationRelativeTo(null);
        dialog.setVisible(true);
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        if (tblPloca.getSelectedRow() < 0) {
            JLabel label = new JLabel("Sistem ne može da obriše izabranu ploču!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(null, label, "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean uspesno = PretragaPlocaKKI.obrisiPlocu(tblPloca);
        
        if (uspesno) {
            JLabel label = new JLabel("Sistem je obrisao izabranu ploču!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(this, label);
        } else {
            JLabel label = new JLabel("Sistem ne može da obriše izabranu ploču!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(null, label, "Greška!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

