    private void btnSacuvajZanrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajZanrActionPerformed
        if (txtZanrID.getText().isEmpty()) {
            JLabel label = new JLabel("Morate kreirati novi žanr!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(null, label, "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (txtNazivZanra.getText().isEmpty()) {
            JLabel label = new JLabel("Morate uneti naziv žanra!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(null, label, "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean uspesno = UnosZanraKKI.sacuvajZanr(txtZanrID, txtNazivZanra);

        if (uspesno) {
            JLabel label = new JLabel("Sistem je uspešno sačuvao žanr!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(this, label);
            dodajUComboBox();
            JComponent comp = (JComponent) evt.getSource();
            Window win = SwingUtilities.getWindowAncestor(comp);
            win.dispose();
        } else {
            JLabel label = new JLabel("Sistem ne može da sačuva žanr!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(null, label, "Greška!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSacuvajZanrActionPerformed


