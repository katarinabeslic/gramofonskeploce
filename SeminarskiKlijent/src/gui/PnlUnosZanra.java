    private void btnKreirajNoviZanrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajNoviZanrActionPerformed
        int zanrID = UnosZanraKKI.vratiID(txtZanrID);

        if (zanrID <= 0) {
            JLabel label = new JLabel("Sistem ne može da kreira novi žanr!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(null, label, "Greška!", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            JLabel label = new JLabel("Sistem je kreirao novi žanr!");
            label.setFont(new Font("Calibri light", Font.PLAIN, 16));
            JOptionPane.showMessageDialog(this, label);
        }

        btnKreirajNoviZanr.setEnabled(false);
    }//GEN-LAST:event_btnKreirajNoviZanrActionPerformed

